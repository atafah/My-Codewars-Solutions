//Surah al-Anbiya
//30. Do the unbelievers not see that the heavens and the earth were joined together, 
//and We split them apart? And We made from water every living thing. 
//Will they not believe?
  
  return age >= 21 ? "drink whisky" : age >= 18 ? "drink beer" : age >= 14 ? "drink coke" : "drink toddy";
